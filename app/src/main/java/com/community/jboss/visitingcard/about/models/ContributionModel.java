package com.community.jboss.visitingcard.about.models;

public class ContributionModel {
    private String name, avatar, contributorurl;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContributorurl() {
        return contributorurl;
    }

    public void setContributorurl(String contributorurl) {
        this.contributorurl = contributorurl;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
}
